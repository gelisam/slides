>>> List(1,2,3).monoMap { x => x + 1 }
List(2, 3, 4)

>>> Set(1,2,3).fmap { x => s"${x}!" }
Set(1!, 2!, 3!)

>>> import scala.collection.immutable.BitSet
>>> BitSet(1,2,3).mapTo[SortedSet] { x => s"${x}!" }
SortedSet(1!, 2!, 3!)




























































































