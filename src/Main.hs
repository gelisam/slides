div' :: Int -> Int -> Maybe Int
div' _ 0 = Nothing
div' x y = Just (x `div` y)

doMath :: Int -> IO ()
doMath n = do
    case 42 `div'` n of
      Nothing -> putStrLn "divided by zero"
      Just x  -> print (x + 1)

main :: IO ()
main = doMath 0


































































































